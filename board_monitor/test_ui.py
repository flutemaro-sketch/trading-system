def main():
    root = tk.Tk()
    root.configure(bg="black")
    data_queue = Queue()

    win = MainWindow(root, data_queue, kabu_client=None)

    # 各ユニットに Aリストを設定
    for unit_key, a_list in UNIT_A_LIST.items():
        for slot_idx, (code, name) in enumerate(a_list):
            win.set_a_slot(unit_key, slot_idx, code, name)

    # ダミーデータ送信
    def send_dummy():
        for code, params in STOCK_DATA.items():
            base, open_p, vol, ask_t, bid_t, prev = params
            data_queue.put({
                "type":       "update",
                "stock_code": code,
                "board_data": make_board(base, open_p, vol, ask_t, bid_t, prev),
            })
        root.after(3000, send_dummy)

    root.after(1500, send_dummy)

    print("=" * 60)
    print("板監視システム UI テスト（v6）")
    print("=" * 60)
    print("10ユニット × 5銘柄 = 50銘柄")
    print("Aリストのボタンをクリックすると板が切り替わります")
    print("=" * 60)

    root.mainloop()


if __name__ == "__main__":
    main()
