# ── ダミーデータ生成 ──────────────────────────────────────────

def make_board(base: float, open_p: float, volume: int,
               ask_total: int, bid_total: int,
               prev_close: float) -> dict:
    """ダミー板データを生成（リアルな板データ）

    価格: 整数ステップ（重複なし）
    数量: 最良値（スプレッド近く）は少量、遠いほど増加
    OVER/UNDER: 板外の大量注文（大きな値）
    """
    base_i = int(base)

    # 価格ステップ: 価格帯によって変える
    if base_i >= 30000:
        step = 10
    elif base_i >= 3000:
        step = 1
    elif base_i >= 300:
        step = 1
    else:
        step = 1

    # 売り気配 10段（スプレッド近くが少量、遠いほど増加）+ OVER
    over_vol  = ask_total * 5
    under_vol = bid_total * 5

    asks = []
    for i in range(10):
        price = base_i + (i + 1) * step
        vol   = max(100, ask_total // 20 + ask_total // 10 * i)
        asks.append({"price": float(price), "volume": vol})
    asks.append({"price": 0, "volume": over_vol})   # 末尾 = OVER集計

    # 買い気配 10段 + UNDER
    bids = []
    for i in range(10):
        price = base_i - (i + 1) * step
        vol   = max(100, bid_total // 20 + bid_total // 10 * i)
        bids.append({"price": float(price), "volume": vol})
    bids.append({"price": 0, "volume": under_vol})  # 末尾 = UNDER集計

    vwap       = (open_p + base) / 2.0
    change_pct = ((base - prev_close) / prev_close * 100) if prev_close else 0.0

    # 通常10段は sign="", 末尾がOVER/UNDER
    asks_sign = [""] * 10 + ["OVER"]
    bids_sign = [""] * 10 + ["UNDER"]

    return {
        "time":           "14:59:59",
        "last_price":     base,
        "change_pct":     change_pct,
        "previous_close": prev_close,
        "open":           open_p,
        "high":           max(base, open_p) + step * 5,
        "low":            min(base, open_p) - step * 5,
        "volume":         volume,
        "vwap":           vwap,
        "time_open":      "09:01:00",
        "time_high":      "10:30:00",
        "time_low":       "14:45:00",
        "asks":           asks,
        "asks_sign":      asks_sign,
        "bids":           bids,
        "bids_sign":      bids_sign,
    }


